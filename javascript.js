	function bulbControl()
	{	
		if (document.getElementById("logo1").getAttribute('src') === 'images/turnon.png')
		{
			document.getElementById("logo1").src="images/turnoff.png"
		}
		else
		{
			document.getElementById("logo1").src="images/turnon.png";
		}
	}
